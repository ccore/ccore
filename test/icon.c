ccPoint iconGetSize()
{
	return (ccPoint){ICON_WIDTH, ICON_HEIGHT};
}

unsigned long *iconGetData()
{
	int i, len;
	unsigned long *data;

	len = ICON_WIDTH * ICON_HEIGHT;
	data = malloc(len * sizeof(unsigned long));

	for(i = 0; i < len; i++){
		// A + R + G + B
		data[i] =
			(iconData[(i << 2) + 3	] << 24) | 
			(iconData[ i << 2		] << 16) | 
			(iconData[(i << 2) + 1	] << 8) | 
			 iconData[(i << 2) + 2	];
	}

	return data;
}
